import * as React from "react";
import { cn } from "@/lib/utils";

export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { interactive?: boolean }
>(({ className, interactive, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-lg border border-(--color-border-default) bg-(--color-bg-secondary) p-6",
      interactive &&
        "transition-colors duration-150 ease-[cubic-bezier(0.16,1,0.3,1)] hover:bg-(--color-bg-tertiary) hover:border-(--color-border-strong) cursor-pointer",
      className,
    )}
    {...props}
  />
));
Card.displayName = "Card";

